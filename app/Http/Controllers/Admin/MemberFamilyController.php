     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'member_id'=>'required'
        ]);
        $familyMember = new MemberFamily();
        $familyMember->family_id = $request->family_id;
        $familyMember->member_id = $request->member_id;
        $familyMember->created_by = $request->user_id;
        $familyMember->save();

        Toastr::success('Giving Category Successfully Saved' ,'Success');
        return redirect()->back();
    }

